public class StairCase {

    public static void main(String[] args) {
        printStairs();

            }

            public static void printStairs() {
                for (int allStairs=1; allStairs<=1; allStairs++) {
                    
                    for (int stair = 1; stair <= 1; stair++) {
                        System.out.println("*          *");
                    } 
                    
                    for (int stair = 1; stair <= 1; stair++) {
                        System.out.println("**        **");
                    }    
                    
                    for (int stair = 1; stair <= 1; stair++) {
                        System.out.println("***      ***");

                    }
                    
                    for (int stair = 1; stair <= 1; stair++) {
                        System.out.println("****    ****");

                    }
                  
                    for (int stair = 1; stair <= 1; stair++) {
                        System.out.println("*****  *****");

                    }
                }
            }
        }